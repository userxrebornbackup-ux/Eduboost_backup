def _set_refresh_cookie(response: Response, refresh: str) -> None:
    """Set refresh token in HTTP-only cookie."""
    response.set_cookie(
        key=REFRESH_COOKIE,
        value=refresh,
        max_age=60 * 60 * 24 * 30,
        secure=True,
        httponly=True,
        samesite="strict",
    )


